package enkapsulasi;

public class Animal {
    private String namaHewan;
    private int umur;

   public void setNamaHewan(String namaHewan) {
    this.namaHewan = namaHewan;
   }

   public String getNamaHewan() {
    return namaHewan;
   }

   public void setUmurKucing(int umur) {
    this.umur = umur;
   }

   public int getUmurKucing() {
    return umur;
   }

}
